        private static void AppendTreatmentTrainSection(StringBuilder sb, TreatmentTrainReportSection section)
        {
            WaterQualityEngine.TreatmentTrainResult train = section.Result;
            sb.AppendLine("<h2>BMP Treatment Train</h2>");
            sb.AppendLine("<p>Method: EMC event loads from catchments; sequential BMP mass balance ");
            sb.AppendLine("(η<sub>total</sub> = 1 &minus; &prod;(1 &minus; η<sub>i</sub>)).</p>");
            sb.AppendLine(string.Format(CultureInfo.InvariantCulture,
                "<p>Land use = <strong>{0}</strong>, runoff depth = {1:0.###} in, area = {2:0.000} ac.<br/>",
                ReportWriterCommon.EscapeHtml(section.LandUse), section.RunoffDepthIn, section.DrainageAreaAcres));
            sb.AppendLine("Chain: " + string.Join(" &rarr; ",
                section.BmpChain.Select(k => ReportWriterCommon.EscapeHtml(BmpLibrary.GetBmp(k).Name))) + "</p>");

            sb.AppendLine("<table><thead><tr>");
            sb.AppendLine("<th>Pollutant</th><th>Influent (lbs)</th><th>Effluent (lbs)</th>");
            sb.AppendLine("<th>Removed (lbs)</th><th>Overall η (%)</th>");
            sb.AppendLine("</tr></thead><tbody>");
            foreach (string pollutant in Pollutant.Core)
            {
                train.InitialLoadsLbs.TryGetValue(pollutant, out double influent);
                train.FinalEffluentLbs.TryGetValue(pollutant, out double effluent);
                train.TotalRemovedLbs.TryGetValue(pollutant, out double removed);
                train.OverallRemovalEfficiency.TryGetValue(pollutant, out double eta);
                sb.AppendLine(string.Format(CultureInfo.InvariantCulture,
                    "<tr><td>{0}</td><td>{1:0.####}</td><td>{2:0.####}</td><td>{3:0.####}</td><td>{4:0.1}</td></tr>",
                    pollutant, influent, effluent, removed, eta * 100.0));
            }
            sb.AppendLine("</tbody></table>");

            sb.AppendLine("<h3>Treatment train calculation steps</h3><div class=\"steps\">");
            foreach (CalcStep step in train.Steps)
                sb.AppendLine(ReportWriterCommon.EscapeHtml(step.ToString()) + "<br/>");
            sb.AppendLine("</div>");
        }

