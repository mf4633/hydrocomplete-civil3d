        private static void AppendStormwaterSections(StringBuilder sb, StormwaterReportData data)
        {
            if (data.Detention != null)
                AppendDetentionSection(sb, data.Detention);
            if (data.BmpSizing != null)
                AppendBmpSizingSection(sb, data.BmpSizing);
            if (data.TreatmentTrain != null)
                AppendTreatmentTrainSection(sb, data.TreatmentTrain);
            if (data.SedimentBasin != null)
                AppendSedimentBasinSection(sb, data.SedimentBasin);
        }

        private static void AppendDetentionSection(StringBuilder sb, DetentionReportSection section)
        {
            DetentionRouting.RoutingResult routing = section.Result;
            sb.AppendLine("<h2>Detention Pond Routing</h2>");
            sb.AppendLine("<p>Method: Modified Puls (storage indication). Inflow from SCS unit hydrograph ");
            sb.AppendLine("scaled by composite SCS CN runoff depth.</p>");
            sb.AppendLine(string.Format(CultureInfo.InvariantCulture,
                "<p>Storage: <strong>{0}</strong><br/>Drainage area = {1:0.000} ac, Tc = {2:0.0} min, " +
                "runoff depth = {3:0.###} in.</p>",
                ReportWriterCommon.EscapeHtml(section.StorageDescription),
                section.DrainageAreaAcres, section.SystemTcMinutes, section.RunoffDepthIn));

            if (section.OutletDescriptions.Count > 0)
            {
                sb.AppendLine("<p>Outlets: ");
                sb.AppendLine(string.Join(", ",
                    section.OutletDescriptions.Select(ReportWriterCommon.EscapeHtml)));
                sb.AppendLine("</p>");
            }

            sb.AppendLine("<table><thead><tr>");
            sb.AppendLine("<th>Q<sub>in,peak</sub> (cfs)</th><th>Q<sub>out,peak</sub> (cfs)</th>");
            sb.AppendLine("<th>Attenuation (%)</th><th>S<sub>max</sub> (ft³)</th><th>Peak elev (ft)</th>");
            sb.AppendLine("</tr></thead><tbody><tr>");
            sb.AppendLine(string.Format(CultureInfo.InvariantCulture,
                "<td>{0:0.00}</td><td>{1:0.00}</td><td>{2:0.1}</td><td>{3:0}</td><td>{4:0.##}</td>",
                routing.PeakInflowCfs, routing.PeakOutflowCfs, routing.ReductionPercent,
                routing.PeakStorageFt3, routing.PeakElevationFt));
            sb.AppendLine("</tr></tbody></table>");

            if (routing.Ordinates.Count > 0)
            {
                sb.AppendLine("<h3>Routing hydrograph</h3>");
                sb.AppendLine("<table><thead><tr>");
                sb.AppendLine("<th>t (hr)</th><th>Q<sub>in</sub> (cfs)</th><th>Q<sub>out</sub> (cfs)</th>");
                sb.AppendLine("<th>S (ft³)</th><th>Elev (ft)</th>");
                sb.AppendLine("</tr></thead><tbody>");
                foreach (DetentionRouting.RoutingOrdinate ord in routing.Ordinates)
                {
                    sb.AppendLine(string.Format(CultureInfo.InvariantCulture,
                        "<tr><td>{0:0.##}</td><td>{1:0.00}</td><td>{2:0.00}</td><td>{3:0}</td><td>{4:0.##}</td></tr>",
                        ord.TimeHours, ord.InflowCfs, ord.OutflowCfs, ord.StorageFt3, ord.ElevationFt));
                }
                sb.AppendLine("</tbody></table>");
            }

            sb.AppendLine("<h3>Routing calculation steps</h3><div class=\"steps\">");
            foreach (CalcStep step in routing.Steps)
                sb.AppendLine(ReportWriterCommon.EscapeHtml(step.ToString()) + "<br/>");
            sb.AppendLine("</div>");
        }

